					<h2>Life Cycle of Frog</h2>
					<p>
					The metamorphosis is another word for the changes an animal makes during it’s life cycle. During frogs metamorphosis , an egg will hatch into a tadpole, which will then develop back legs first, then front legs, and become fully grown adults.
					</p>
					<b><mark> Stage: 1 (EGG)</mark></b>
					<p> Frog lays eggs. That’s where new frogs come from. A female can lay up to 4000 eggs at a time! The eggs float on water in a jelly mass or cluster. The eggs hatch in one to three weeks into tadpoles.</p>
					<b><mark> Stage: 2 (TADPOLES)</mark></b>
					<p> The tadpoles hatch from the eggs. When the tadpole hatches, it looks like a fish than a frog. A tadpole lives in the water, it uses gills to breathe and it has no legs. The tadpole swims, eats plants and algae from the water, and grows for several weeks.</p>
					<b><mark> Stage: 3 (FROGLET)</mark></b>
					<p> During this stage, the tadpole starts to develop lungs, so it will be able to breathe out of the water when it becomes a frog. At this point, the tadpole starts to grow two hind legs. At this point tadpole can leap around instead of only swim. Although the froglet is starting to look like a frog, it still have a very long tail. </p>
					<b><mark>Stage: 4(YOUNG FROG)</mark></b>
					<p> Froglet grows two front legs and it’s long tail becomes shorter and shorter. The tadpole uses the nutrients stored in its tail as food, so until it’s tail is completely gone, it doesn’t need anything else to eat! Then just a little stub of its tail is left, and the froglet is young frog. It hops out of the water and onto dry land for the first time! The frog is still very small. </p>
					<b><mark>Stage: 5 (ADULT FROG)</mark></b>
					<p> The frog’s tail will eventually disappear completely and it will start to eat insect instead of plants from the water. The young frog will grow for about 2-4 years to become an adult. The adult lays eggs and more tadpole hatches and begin the cycle again!</p>
