					It is also Commonly called as Portuguese man of war.
And also named because of its sudden appearance and disappearance like Portuguese ships during war. 
Most beautiful siphonophore having irredescent peacock, blue or orange colour.  
It’s Size range upto 10 to 30 cm long but tentacles measure several metres.
Animal is composed of upper large crested pneumatophore or float and lower various zooids.  
Pneumatophore contains gas glands which secrete air composed off 7.5% to 13.5% oxygen, 1·5% argon and 85% to 91% nitrogen.  
Gas fills the float to swim or float and when animal has to sink, gas is forced out through the pneumatophore. 
Ventrally the float contains budding coenosarc from which dactylozooids, gastrozooids, gonozooids and gonodendra hang down. 
 A portion of cluster of zooids reveal large fishing tentacles with batteries of pneumatocyst, gastrozooids without tentacles, large dactylozooids, small dactylozooids, gonodendra and large gelatinous zooids of unknown function.
 A part of gonodendra shows upper small male gonophores, lower large rounded female gonophores with eggs. Along with gonophores are several cylindrical gonopulpons.

