							<h1>Did you know? </h1>
	<h2>Water scorpions are carnivores, which eat other pond animals making them higher on the food chain.
	Most pond insects form the second level of the food chain, these insects eat plant matter and are food to many other organisms. 
	They are classified as predacious creatures by grabbing other organisms by their front legs and suck out the body fluids. 
	Adults bury themselves in mud during time of drought until conditions improve. 
	They usually pass the winter on the bank under stones and remnants of plants, or in the water beneath the ice, where they can live for a long time almost without oxygen. 
	Nepa cinerea have three disc shaped static sense organs located on the ventral side of the thorax. 
	These are used to gauge depth and compensate for changes in water pressure. 
	Water scorpions can tolerate low levels of dissolved oxygen, by coming to the surface to breathe or by using their long breathing tubes. 
	The adults and nymph water scorpions can communicate by rubbing the base of their legs against their body to make a squeaky noise.
	This mechanism is also used by males to attract the females.
