					<h2>Gorgonia</h2>
					<p>
					G. ventalina is a fan-shaped colonial coral with several main branches and a latticework of linking smaller branches. 
					The skeleton is composed of calcite and gorgonion, a collagen-like compound.  
					The calyces in which the polyps are embedded are in two rows along the branches. 
					Many of the smaller branches are compressed in the plane of the fan, which distinguishes this species from the Venus Sea fan (Gorgonia flabellum). 
					It often has small accessory fans growing out sideways from the main fan. It grows to 1.5 m (5 ft) tall and is variable in colour, being whitish, yellow, or pale purple.  
					The main branches are often purple and the fan is orientated at right angles to the current. 
					It also contains certain secondary metabolites in its tissues which are distasteful. 
					This coral is sometimes attacked by the fungus Aspergillus sydowii which causes aspergillosis. 
					This results in damaged patches, galls, purpling of the tissues, and even coral death. 
