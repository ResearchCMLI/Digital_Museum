					<h2>Albunea</h2>
					<p>
					Albunea carabus is typically up to 2 centimetres (0.8 in) long.  
					In common with other species of Albunea, it has a quadrangular carapace with a concave rear edge, and flattened legs, which it uses for digging in sand.
					The first pair of pereiopods are subchelate (forming a claw with one movable finger pressing onto a solid edge, rather than a fixed finger), and all five pairs are flattened into shovels and are effective at digging. 
					The pleon (abdomen) is shorter than the carapace, and bears four pairs of pleopods in females, but none in males. 
					The pleon ends in a telson flanked by a pair of uropods, which are similar in form to the pereiopods, and aid in digging. 
					The larvae of Albunea carabus are likely to pass through five planktonic zoea instars before reaching the juvenile stage, as also seen in Albunea symmysta. 
					Over the first three of these instars, the larvae increase from around 1.56 mm to around 2.72 mm in total length. 
					By the third zoea instar, the pereiopods begin to show as small buds, and uropods are present alongside the broad telson.
					</p>
