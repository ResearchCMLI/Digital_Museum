					<h2>Dracunculus Medinensi</h2>
					<p>
				Dracunculus is a genus of spiruroid nematode parasites in the family  dracunculidae. All members of Dracunculus are obligate parasites of mammals or reptiles.Adult females reside just under the skin, and eventually form a blister in the host’s skin through which they access the environment. Adult female Dracunculus worms are noted for their extraordinary length, with some growing up to 100 cm long. Males are much smaller (16 – 40 mm) and are relatively rare  in some species the male has never been described. Adults of both sexes are narrow yellow-white colored worms, with a rounded front-end, and a conical tail-end with a pointed tip. Females of different species within the genus tend to look similar, and can rarely be distinguished on morphology alone. The body of a fertilized adult female is almost completely filled by its uterus.The majority of Dracunculus species described infect reptiles  predominantly snakes.There are 14 accepted Dracunculus species, 10 of which infect reptiles just four dracanculus species are known to infect mammals, of which the best known is the human parasite D. medinensis.
