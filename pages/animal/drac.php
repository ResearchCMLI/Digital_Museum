					<h2>Habitat and conservation</h2>
					<p >
					Dracunculus worms are distributed globally, though each species has a narrower range. Dracunculus medinensis favors freshwater habitats, mainly stagnant waters such as ponds, cisterns, pools in dried up riverbeds, and temporary hand-dug wells. These habitats are reservoirs for copepods, the vector of the Guinea worm disease. Known cases of dracunculiasis, or the Guinea worm disease, are in sub-Saharan Africa where the climate is dry at least part of the year, creating the perfect conditions for warm, stagnant, shallow waters like the ones where D. medinensis larvae are commonly found.The environment within the D. medinensis copepod host varies. In some hosts the Dracunculus larvae remain relatively unharmed. They survive in the gut of the crustacean and live off of its host’s resources without major harm to the copepod. In other cases, the copepod will attempt to digest its occupant. These typically target intermediate hosts that are less likely to reject their occupancy or die due to the added burden of sharing its resources. D. medinensis was historically widespread in Africa and South Asia, but is now limited to dozens of cases annually in humans and domestic dogs, and may soon be driven to extinction. 
