							<h1>Did you know? </h1>
	<h2>Dracunculus medinensis, or Guinea worm, is a nematode that causes dracunculiasis, also known as guinea worm disease. The disease is caused by the female which, at up to 80 centimetres in length,is among the longest nematodes infecting humans. In contrast, the longest recorded male Guinea worm is only 4 cm. The worms can reach a metre in length. If one simply pulls off the protruding head of the worm, the worm will break and leak high levels of foreign antigen which can lead to anaphylactic shock and fast death of the host. Hence it is important to remove the worm slowly (over a period of weeks). This is typically undertaken by winding the worm onto a stick by a few centimetres each day. Guinea worm is on target to be the second infectious disease of humans to be eradicated, after smallpox. The common name ”guinea worm” is derived from the Guinea region of Western Africa.

