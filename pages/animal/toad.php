							<h1>Did you know? </h1>
	<h2>Toads are similar to frogs with some distinct features that separate them. Toads of different species look different. 
	For example, the cane toad looks different to the American toad. These animals from the order Anura have warts on their bodies, are poisonous, and survive by eating worms, insects, and mollusks. Toads are mostly solitary animals. They come together in the breeding season like frogs when large numbers of Bufo toads congregate on breeding ponds. This forms a battleground with male toads making loud croaking sounds to attract females. Frogs and toads show similar kinds of social behavior.
	Different toad species have different lifespans. The American toads live for five to 10 years in the wild, whilst oriental fire-bellied toads can live up to 20 years in their natural habitats. The fire-bellied toad's habitat spreads from northeastern China to North and South Korea, and some regions of Russia. Cane toads have a lifespan of 10 to 15 years. 
	Although their habits and breeding style are almost the same, frog eggs and toad eggs look different. 
	Frog eggs float in round clusters on the surface of the water whilst toad eggs remain attached to leaves and blades of grass near the edge of the water.  
	Their eggs are arranged in parallel long strands resembling round black beads. Every species of toad has a unique mating call to attract females of the same species, and toads and frogs not only croak loudly when they want to attract females but also when they are scared after being touched, or if they are angry. 
	Usually, toad sounds act as danger signals, producing a high-pitched scream. Make toads screech louder than females and at night toads croak to signal other toads to stay away.

