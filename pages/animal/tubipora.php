					<h2>Habitat and conservation</h2>
					<p >
					Organ pipe corals are restricted to shallow waters, and tend to live in sheltered areas where they eat plankton. Furthermore, it is usually found in habitats ranging from 2 to 20 m in depth. 
					Notably, they have been found dominating bedrock and sandy grooves at 8 to 10 m, as well as mid-shelf reefs. 
					They are also widely distributed, having been found in the west Pacific, south of Japan, west to Africa's East Coast, as well as throughout the Red Sea.
					The species prefers good, bright light conditions, as well as medium to strong water currents and well oxygenated water. 
					To be successful, they also require sufficient alkalinity and calcium. 
					Colonies of organ pipe corals are interconnected networks, consisting of many pipes and tubes joined together with parallel platforms. 
					Furthermore, the colonies are usually composed of polyps that are all one colour, although some large colonies have been found to have a mixture. 
					When this occurs, there are usually obvious sections of specific colors in the colony; different coloured polyps do not live interspersed with each other.
					
