							<h1>Did you know? </h1>
	<h2>Native to the Indian Ocean, Organ Pipe Coral has many horizontal, organ-pipe like tubes which give it the name.  A soft coral, it is often mistaken for a hard coral because of its red, external skeleton.  This colorful skeleton is usually obscured in the daytime by extended polyps.  Organ Pipe Coral polyps are often grey or green but are also seen in white, silver, light blue or purple.
Capable of fast growth in the right conditions, this species isn’t aggressive enough to take over a tank. Organ Pipe Coral is listed as a near-threatened species with 50% of the population lost in the last 10 years, according to the IUCN. Species categorized as “Near Threatened (NT)” are at risk of becoming endangered in the future but don’t currently qualify for threatened status.
Organ Pipe Coral is valued for jewellery and has high medical and scientific value. 

