					<h2>PARTS OF FROG</h2>
					<b><mark>  1.HEART</mark></b>
					<p>The frog’s heart is three-chambered and it has no partition that separates oxygenated and deoxygenated blood. It has two main chambers namely – Sinus Venosus which receives blood and Conus Arteriosus which departs blood.The primary function of the heart is to pump oxygen rich blood to organs such as the brain, liver, and kidneys as well as all other tissue.</p>
					<b><mark> 2.GALL BLADDER </mark></b>
					<p> Gall bladder of frog is present under the liver. It is a small green sac, which stores bile. The gall bladder stores bile and releases it into the duodenum via the bile duct. Bile functions in the digestion of fats.</p>
					
					<b><mark> 3.LIVER</mark></b>
					<p> It is reddish-brown in colour.It is multi-lobed gland and lies close to the heart and lungs. 3 lobes are present in the liver of frog i.e. right, left and median. The liver also produces the bile that is stored in the gall bladder, and used in the digestion of fats.</p>
					<b><mark> 4.STOMACH</mark></b>
					<p> Stomach is present on the left side in the body cavity. It is attached to the dorsal body wall by a mesentery termed as mesogaster. It is around 4 cm long, broad and slightly curved bag or tube with thick muscular walls.</p>
					<b><mark> 5.SMALL INTESTINE</mark></b>
					<p> Small intestine is a long, coiled and narrow tube. It is about 30cm long, and is attached mid-dorsally to body wall by mesenteries. Duodenum runs ahead being parallel to stomach and forms a “U” shaped structure. Ileum is the longest part of alimentary canal.</p>
					<b><mark> 6.LARGE INTESTINE</mark></b>
					<p> Large intestine is short, wide tube about 4cm long. It runs straight behind to open into cloaca by anus. The opening is guarded by an anal sphincter. The inner lining of large intestine forms numerous low longitudinal folds. It serves for the re-absorption of water and the preparation and storage of faeces. </p>
