							<h1>Did you know? </h1>
	<h2>Virgularia is bioluminescent.
	As is the case for all octocorals, sea pens are actually colonies of polyps.  
	One polyp grows very large and loses its tentacles, forming the central axis.
	The central axial polyp is divided into two regions: a lower peduncle or stalk, which never bears secondary polyps and functions as a burrowing organ, and an upper stem or rachis, from which numerous secondary polyps bud. 
	Some of these secondary polyps, called autozooids, are typical feeding polyps. 
	Others, the larger and fewer siphonozooids, serve as intakes for water, which circulates within the colony and helps keep it upright.
	The axial polyp contains a slender, unbranched, calcareous skeletal rod (axis). 
	In this species the axis is round in section and often protrudes from the top of the colony.
