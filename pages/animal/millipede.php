					<h2>Habitat and conservation</h2>
					<p >
					Millipedes occur on all continents except Antarctica, and occupy almost all terrestrial habitats, ranging as far north as the Arctic Circle in Iceland, Norway, and Central Russia, and as far south as Santa Cruz Province, Argentina.
					Typically forest floor dwellers, they live in leaf litter, dead wood, or soil, with a preference for humid conditions. 
					In temperate zones, millipedes are most abundant in moist deciduous forests, and may reach densities of over 1,000 individuals per square metre.
					Other habitats include coniferous forests, caves, and alpine ecosystems. 
					Deserticolous millipedes, species evolved to live in the desert, like Orthoporus ornatus, may show adaptations like a waxy epicuticle and the ability of water uptake from unsaturated air. Some species can survive freshwater floods and live submerged underwater for up to 11 months. A few species occur near the seashore and can survive in somewhat salty conditions.
