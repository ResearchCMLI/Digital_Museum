							<h1>Did you know? </h1>
	<h2>
	Millipedes are among the first animals to have colonised land during the Silurian period. Early forms probably ate mosses and primitive vascular plants. 
	Millipedes do not bite, and their defensive secretions are mostly harmless to humans — usually causing only minor discolouration on the skin — but the secretions of some tropical species may cause pain, itching, local erythema, edema, blisters, eczema, and occasionally cracked skin.
	Due to their lack of speed and their inability to bite or sting, millipedes' primary defence mechanism is to curl into a tight coil – protecting their delicate legs inside an armoured exoskeleton. Millipedes appear in folklore and traditional medicine around the world. 
