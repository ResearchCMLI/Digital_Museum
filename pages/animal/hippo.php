	<h2>Seahorses have no teeth and no stomach.
	They can consume 3,000 or more brine shrimp per day. 
	Seahorses are monogamous and mate for life. 
	Rarer still, they are among the only animal species on Earth in which the male bears the unborn young.
	When mating, the female seahorse releases up to 50 eggs into a pouch on the male’s abdomen. 
	The male seahorse carries the eggs in his pouch until they hatch, then releases fully formed, miniature seahorses into the water. 
	As little as 5 or as many as 1,500 young can be born. Male pregnancy frees to female to make more eggs straight away and so reproduce quicker.
