					<h2>Jellyfish</h2>
					<p>
					 As invertebrates they are, they lack a backbone and have two layers of tissue: an external ectoderm and an internal endoderm.
					 In the middle of these layers is the gelatinous mesoglea which provides volume and support for the animal.
					 The jellyfish´s body for most of their life cycle has the shape of a bell. In the case of box jellyfish, the bell has four different sides. 
					 Extending from the body of most jellyfish are long tentacles embedded with nematocysts. Jellyfish also lack head, circulatory system and organs for respiration and excretion. 
					 Their mouth is in the lower part of their body, and is connected to a central cavity filled with hair-like structures called cilia that help transport food and other materials throughout the body. 
					 Their lower muscles contract and expand the body, like an umbrella, allowing the animal to swim.  
					 A network of nerves runs under the body´s coat and coordinates the muscles. Some true jellyfish have simple eyes around the edge of their body.  
					 Some jellyfish are bioluminescent, which means they can shine in the dark. The jellyfish´s bodies generally vary from about 1 to 16 inches (2 to 40 centimetres) in diameter, although some species are much larger, while the tentacles´ number and length vary according to the species.
