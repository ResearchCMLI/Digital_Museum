					<h2>Habitat and conservation</h2>
					<p >
					 Jellyfish are found all over the world, from surface waters to the deep sea. 
					 Though most are found in marine waters, a few have also been found inhabiting the freshwaters. 
					 Jellyfish are mainly free-swimming marine animals with umbrella-shaped bells and trailing tentacles, although a few are anchored to the seabed by stalks rather than being mobile.  
					 The bell can pulsate to provide propulsion for highly efficient locomotion. 
					 Jellyfish drift along the ocean currents, living in cold and warm waters around the world. 
					 Large storms and the movement of strong tides cause the solitary jellyfish animals to become swarmed up in the same habitat.  
					 These natural forces can push them into deep waters, as well as close to the shorelines.
					
