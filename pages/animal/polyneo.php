					<p>
					Polynoe is a type of scale worm. Most of these species are short in nature but some can reach up to 20 cm in length and 10 cm width.It has distinct head and its body comprises of 12-15 segments.The head bears two palps, two pairs of peristomial cirri and three small pro-stomiai tentacles. Its pharynx is protrusible and is armed with small teeth.Individuals are usually covered  by scales entirely. This is also known as elytra, which can be shed and regenerated in many species.Its scales are alternated with dorsal cirri which covers the major part of the body length.Parapodium are present on each body segment in pairs. A ventral neuropodial component and a dorsal notopodial part make up each parapodium. The dorsal plate-like (scale-like) & flat elytra, which is used for respiration, is changed from the notopodial cirrus. 
