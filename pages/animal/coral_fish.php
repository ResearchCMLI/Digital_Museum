					<h2>Coral Fish</h2>
					<p>
					Coral reef fish are fish which live amongst or in close relation to coral reefs. 
					Coral reefs form complex ecosystems with tremendous biodiversity. 
					Most reef fishes have body shapes that are different from open water fishes.
					Open water fish are usually built for speed in the open sea, streamlined like torpedoes to minimise friction as they move through the water. 
					Coral reef fishes exhibit a huge variety of dazzling and sometimes bizarre colours and patterns. 
					This is in marked contrasts to open water fishes which are usually counter shaded with silvery colours. 
					Coral reefs are the result of millions of years of coevolution among algae, invertebrates and fish. 
					They have become crowded and complex environments, and the fish have evolved many ingenious ways of surviving. 
					Most fishes found on coral reefs are ray-finned fishes, known for the characteristic sharp, bony rays and spines in their fins. 
					These spines provide formidable defences, and when erected they can usually be locked in place or are venomous. 
					Many reef fish have also evolved cryptic coloration to confuse predators.
					Many reef fish species have evolved different feeding strategies accompanied by specialized mouths, jaws and teeth particularly suited to deal with their primary food sources found in coral reef ecosystems.
					
