					<h2>Habitat and conservation</h2>
					<p >
					Coral reefs contain the most diverse fish assemblages to be found anywhere on earth, with perhaps as many as 6,000–8,000 species dwelling within coral reef ecosystems of the world's oceans The richest by far in terms of reef fish diversity is the Indo-Pacific where there are an estimated 4,000–5,000 species of fishes associated with coral reef habitats. 
					Another 500–700 species can be found in the greater Caribbean region.
					The mechanisms that first led to, and continue to maintain, such concentrations of fish species on coral reefs has been widely debated over the last 50 years.  While many reasons have been proposed, there is no general scientific consensus on which of these is the most influential, but it seems likely that a number of factors contribute. 
					These include the rich habitat complexity and diversity inherent in coral reef ecosystems, the wide variety and temporal availability of food resources available to coral reef fishes, a host of pre and post-larval settlement processes, and as yet unresolved interactions between all these factors.  The wealth of fishes on reefs is filled by tiny, bottom-dwelling reef fishes.
