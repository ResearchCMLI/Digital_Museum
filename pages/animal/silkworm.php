					<b><mark>  Stage 2 - silkworm</mark></b>
					<p>The second stage of the silkworm's life cycle is when it grows to its greatest size. A hairy silkworm develops after the eggs are cracked. Before entering the third stage, a caterpillar or larvae ingest an enormous number of mulberry leaves for around a month (about 30 days).</p>
					<b><mark>  Stage 3 - Cocoon</mark></b>
					<p>An insect larva creates a cocoon, which is an envelope primarily made of silk, to protect itself while it develops. It is made from a single silk thread and is around the size of a round cotton mall.</p>
					<b><mark> Stage 4 – Pupa</mark></b>
					<p>The pupa stage is the fourth stage of a silkworm’s life cycle. It is understood to be an entirely still procedure. The pupa is killed by the makers by unrolling the silk yarn and dropping the fully completed cocoon into boiling water.</p>
					<b><mark>  Stage 5 - Moth</mark></b>
					<p>The final phase of a silkworm’s life cycle involves it developing from a pupa into an adult moth. The moth’s female reproductive unit makes eggs. The life cycle then restarts at this stage.</p>
