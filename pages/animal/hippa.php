					Rostrum is reduced.  
					Head appendages are a pair of stalked compound eyes, a pair of short antennules a pair of long hairy antennae. 
					The third maxillipeds are broad. 7 pairs of thoracic appendages are present. 
					First 2 pairs are partially chelate, next two pairs are pointed anteriorly and last three pairs are backwardly pointed. 
					A gill is attached with each thoracic legs.  Last one or two pairs of thoracic appendages are usually smaller and often concealed by the carapace. 
					Abdomen is more or less reduced often soft and bent upon itself. The abdominal pleura small.  Abdomen 6-segmented. 
					First 3 abdominal segments bear swimming appendages and produce water current to bathe the gills. 
					The last three are posteriorly directed and are called as uropods, which are used for darting.
