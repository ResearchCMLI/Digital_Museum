					<h2>Tapeworm</h2>
					<p>
					Tapeworms are bilaterally symmetrical (i.e., the right and left sides are similar). 
					Some consist of one long segment; others have a definite head, followed by a series of identical segments called proglottids. 
					The head, or scolex, bears sucker and often hooks, which are used for attachment to the host. 
					The body covering is a tough cuticle, through which food is absorbed. 
					There is neither a mouth nor a digestive tract. 
					Tapeworms also lack a circulatory system and an organ hermaphroditic (i.e., functional reproductive organs of both sexes occur in the same individual). 
					They are usually self-fertilizing, and gonads of both sexes also occur within a single proglottid. 
